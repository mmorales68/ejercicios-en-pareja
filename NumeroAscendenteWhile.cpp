#include <iostream>
using namespace std;
int main() {

    cout << "Bienvenido al programa que calcula los numeros del 1 al 20, a continuacion se le mostrara dicha indicacion" << endl;
    int numero = 1;

    while (numero <= 20) {
        cout << numero << endl;
        numero++;
    }
    cout << "Pase feliz dia" << endl;
    return 0;
}


